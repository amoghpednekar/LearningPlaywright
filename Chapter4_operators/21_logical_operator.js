// && AND gate
// || OR gate

let a = true;
let b = false;

console.log(a && b); // this is false since both are different

//in case of && if both value are same then it will return true
console.log(a && a); // this is true since both are same

console.log(a || b); // this is true since both are different

//in case of || if both value are same then it will return false
console.log(b || b);// this is false since both are same 

// there is also a concept of reverse value
console.log("-----------");

console.log(!a); // this becomes false since a is true
console.log(!b); // this becomes true since b is false




