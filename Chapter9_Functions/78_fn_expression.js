function greet(name) {
    return `Hello, ${name}`;
}
// Functions as Expression, also it is a type 4 function
function greet1(name1) {
    return `Hey, ${name1}`;
}

function greet2(name1) {
    return `Ohh, ${name1}`; //same param is used 
}

console.log(greet("Bob the builder"));
console.log(greet1("Karke dikhayenge"));
console.log(greet2("Wah bhai waah"));


