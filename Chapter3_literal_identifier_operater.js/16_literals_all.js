//integer literal
let num = 10;
console.log(num);
let negative = -90;
console.log(negative);
let float = 10.5;
console.log(float);

let num2 = 0;
console.log(num2);


//bigint literal
let bigInt = 10n;
console.log(bigInt);


//exponential literal
let exp = 1e10;
console.log(exp);


//hexadecimal literal
let hex = 0xff;
console.log(hex);


//octal literal
let octal = 0o77;
console.log(octal);


//binary literal
let binary = 0b1111;
console.log(binary);


//scientific notation literal
let sci = 1.2e-3;
console.log(sci);


//string literal

//empty string
let emptyStr = "";
console.log(emptyStr);

//double quotes
let str1 = "hello";
console.log(str1);

//single quotes
let str2 = 'hello';
console.log(str2);

//check single and double
let str6 = "It's a beautiful day";
console.log(str6);

//backticks
let str3 = `hello`;
console.log(str3);

//string with escape characters
let str4 = "hello\nworld";
console.log(str4);

//string with template literals
let str5 = `hello ${str4}`;
console.log(str5);

//backticks with math
let str7 = `2+2 = ${2 + 2}`;
console.log(str7);

let math = `77+67=${67 + 77}`;
console.log(math);


//boolean literal
let bool = true;
console.log(bool);

