//  loops basically uses increment and decrement operators

// Post increment - where value remains same as assigned then incremented
let a = 10;
b = a++
console.log(b);
console.log(a);


//ERT - Expression result table
// Line no | Variable | Result
// 4 | 10 | NA
// 5 | 10 | 10
// 6 |10 | 10
// 7 |11| 11

//pre increment - this increases the value first and then assigns

let ad = 11;
ad = ++ad;
console.log(ad);
//ERT - Expression result table
// Line no | Variable | Result
// 18 | 11 | NA
// 19 | 12 | 12
// 6 |12 | 12



